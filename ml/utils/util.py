# 用于计算二值掩膜（img_gt）的有符号距离图（Signed Distance Map，SDM）和边界图
def compute_sdf_and_edge(img_gt, out_shape):
    """
    compute the signed distance map of binary mask
    input: segmentation, shape = (batch_size, x, y, z)
    output: the Signed Distance Map (SDM)
    sdf(x) = 0; x in segmentation boundary
             -inf|x-y|; x in segmentation
             +inf|x-y|; x out of segmentation
    normalize sdf to [-1,1]
    """

    img_gt = img_gt.astype(np.uint8)
    normalized_sdf = np.zeros(out_shape) # 用于存储归一化的有符号距离图
    normalized_edge = np.zeros(out_shape) # 用于存储边缘图像

    for b in range(out_shape[0]): # batch size 对于批量中的每一个图像
        posmask = img_gt[b].astype(np.bool_) # 获取当前图像中的正类掩码，并将其转化为布尔类型
        if posmask.any():
            negmask = ~posmask
            posdis = distance(posmask) # 正类掩码中到边界的距离
            negdis = distance(negmask)
            boundary = skimage_seg.find_boundaries(posmask, mode='inner').astype(np.uint8) # 图像中对象的边界
            # 根据公式将正类和负类的距离进行归一化并相减得到SDF
            sdf = (negdis-np.min(negdis))/(np.max(negdis)-np.min(negdis)+ 1e-5) - (posdis-np.min(posdis))/(np.max(posdis)-np.min(posdis)+ 1e-5)
            sdf[boundary==1] = 0 # 将边界上的sdf数值设为0
            normalized_sdf[b] = sdf
            normalized_edge[b] = boundary
            # assert np.min(sdf) == -1.0, print(np.min(posdis), np.max(posdis), np.min(negdis), np.max(negdis))
            # assert np.max(sdf) ==  1.0, print(np.min(posdis), np.min(negdis), np.max(posdis), np.max(negdis))

    return normalized_sdf, normalized_edge


def select_device(device='', apex=False, batch_size=None):
    # device = 'cpu' or '0' or '0,1,2,3'
    cpu_request = device.lower() == 'cpu'
    if device and not cpu_request:  # if device requested other than 'cpu'
        os.environ['CUDA_VISIBLE_DEVICES'] = device  # set environment variable
        assert torch.cuda.is_available(), 'CUDA unavailable, invalid device %s requested' % device  # check availablity

    cuda = False if cpu_request else torch.cuda.is_available()
    if cuda:
        c = 1024 ** 2  # bytes to MB
        ng = torch.cuda.device_count()
        if ng > 1 and batch_size:  # check that batch_size is compatible with device_count
            assert batch_size % ng == 0, 'batch-size %g not multiple of GPU count %g' % (batch_size, ng)
        x = [torch.cuda.get_device_properties(i) for i in range(ng)]
        s = 'Using CUDA ' + ('Apex ' if apex else '')  # apex for mixed precision https://github.com/NVIDIA/apex
        for i in range(0, ng):
            if i == 1:
                s = ' ' * len(s)
            print("%sdevice%g _CudaDeviceProperties(name='%s', total_memory=%dMB)" %
                  (s, i, x[i].name, x[i].total_memory / c))
    else:
        print('Using CPU')

    print('')  # skip a line
    return torch.device('cuda:0' if cuda else 'cpu')

